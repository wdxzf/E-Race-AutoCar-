// 函数简介     获取 IMU660RA 陀螺仪物理量数据
// 参数说明     *measurement_data       IMU660RA 的测量数据缓冲指针
// 返回参数     range                   IMU660RA 加速度量程 详见 zf_device_imu660ra.h 中 imu660ra_gyro_range_enum 定义
// 参数说明     *physical_data          IMU660RA 的物理量数据缓冲指针
